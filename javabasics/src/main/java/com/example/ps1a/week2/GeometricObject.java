package com.example.ps1a.week2;

public class GeometricObject {

    private String color;
    private Boolean filled;

    public void setColor(String color) {
        this.color = color;
    }

    public void setFilled(Boolean isFilled) {
        filled = isFilled;
    }

    public Boolean isFilled() {
        return filled;
    }

}
